export const legalSources = [
  ...federalLegalSources,
  ...stateStatuteSources,
] satisfies LegalSource[];

export function getStateStatuteSource(stateCode: string) {
  const normalizedStateCode = stateCode.toUpperCase();

  return stateStatuteSources.find(
    (source) => source.stateCode === normalizedStateCode,
  );
}

export function getLegalSourcesByJurisdiction(
  jurisdiction: LegalSourceJurisdiction,
) {
  return legalSources.filter((source) => source.jurisdiction === jurisdiction);
}
